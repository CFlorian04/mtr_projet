GAME_WIDTH = 800
GAME_HEIGHT = 600

def getGameWidth():
    global GAME_WIDTH
    return GAME_WIDTH


def getGameHeight():
    global GAME_HEIGHT
    return GAME_HEIGHT


def setGameWidth(width):
    global GAME_WIDTH
    GAME_WIDTH = width


def setGameHeight(height):
    global GAME_HEIGHT
    GAME_HEIGHT = height
